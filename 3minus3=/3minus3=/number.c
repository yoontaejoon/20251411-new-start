#include <stdio.h>

int main() 
{
    int a = 3;
    int b = 4;
    int sum = a + b;
    int sub = a - b;
    int mul = a * b;
    int div = a / b;
    printf("SUM of %d and %d is %d\n", a, b, sum);
    printf("SUM of %d and %d is %d\n", a, b, sub);
    printf("SUM of %d and %d is %d\n", a, b, mul);
    printf("SUM of %d and %d is %d\n", a, b, div);

    return 0;
}
